export type LanguageCode = "en" | "zh";
export type LanguageMode = "system" | LanguageCode;
export type ThemeMode = "system" | "light" | "dark";

export interface AppSettings {
  languageMode: LanguageMode;
  themeMode: ThemeMode;
}

export const defaultAppSettings: AppSettings = {
  languageMode: "system",
  themeMode: "system"
};

export function resolveLanguage(languageMode: LanguageMode, deviceLanguage: LanguageCode): LanguageCode {
  return languageMode === "system" ? deviceLanguage : languageMode;
}

export function languageFromLocale(languageCode?: string | null): LanguageCode {
  return languageCode?.toLowerCase().startsWith("zh") ? "zh" : "en";
}

